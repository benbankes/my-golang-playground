func countValidPasswords(users []user) int {
	count := 0

	for _, aUser := range users {
		if isValid(aUser.password, aUser.policy) {
			count++
		}
	}

	return count
}

func isValid(password string, policy passwordPolicy) bool {
	actual := strings.Count(password, policy.character)

	return actual <= policy.max && actual >= policy.min
}
